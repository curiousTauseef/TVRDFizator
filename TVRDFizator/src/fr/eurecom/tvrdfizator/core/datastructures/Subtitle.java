package fr.eurecom.tvrdfizator.core.datastructures;

public class Subtitle {
	private float startTmp;
	private float endTmp;
	private String text;
	
	
	
	
	public float getStartTmp() {
		return startTmp;
	}
	public void setStartTmp(float startTmp) {
		this.startTmp = startTmp;
	}
	public float getEndTmp() {
		return endTmp;
	}
	public void setEndTmp(float endTmp) {
		this.endTmp = endTmp;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
	
	
}
